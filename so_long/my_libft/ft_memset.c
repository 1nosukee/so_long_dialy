#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*k;

	i = 0;
	k = (char *)b;
	while (i < len)
	{
		k[i] = c;
		i++;
	}
	return (k);
}
